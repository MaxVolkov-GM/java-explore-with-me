DROP TABLE IF EXISTS endpoint_hits;

CREATE TABLE IF NOT EXISTS endpoint_hits (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    app VARCHAR(255) NOT NULL,
    uri VARCHAR(512) NOT NULL,
    ip VARCHAR(64) NOT NULL,
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_endpoint_hits_created
    ON endpoint_hits (created);

CREATE INDEX IF NOT EXISTS idx_endpoint_hits_uri
    ON endpoint_hits (uri);

CREATE INDEX IF NOT EXISTS idx_endpoint_hits_uri_created
    ON endpoint_hits (uri, created);